def bank(X, Y):
    """
    Функция для расчета суммы вклада с учетом сложных процентов.
    
    :param X: начальная сумма вклада
    :param Y: количество лет
    :return: итоговая сумма на счету
    """
    # Процентная ставка
    r = 10
    # Расчет итоговой суммы по формуле сложных процентов
    A = X * (1 + r / 100) ** Y
    return A

# Пример использования функции
initial_deposit = float(input("Введите начальный вклад (X): "))
years = int(input("Введите количество лет (Y): "))

result = bank(initial_deposit, years)

print(f"Сумма на счету спустя {years} лет: {result:.2f}")

