# Запрашиваем у пользователя возраст
my_age = input("Введите ваш возраст: ")

# Выводим сообщение с введенным возрастом
print("Ваш возраст:", my_age)

# Преобразуем строку в целое число и увеличиваем на 1
my_age = int(my_age) + 1

# Выводим обновленный возраст
print("Ваш возраст через год:", my_age)
